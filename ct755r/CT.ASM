main    proc    far
;---------------------------------------------------------------------------
; in this section, for checking DOS version we use old stile
; functions, for compatibility with DOS 1.X
	mov	ah, 30h		; get DOS version in AL
	int	21h
	cmp	al, 02
	jae @F			; if DOS version above or equal 2.00, work
	push	ds		; move to stack the para of PSP
	mov	ax, 0		; for return to DOS by RETF
	push	ax		; move to stack zero offset of address
	mov	ax, data	; init ds, for print string function
	mov	ds, ax
	mov	dx, offset error_dosver_str
	mov	ah, 09h		; print error string
	int	21h
	retf			; old style return to DOS

;---------------------------------------------------------------------------
@@:
	mov	ax, data
	mov	ds, ax

if MEM_REALLOC 
; release unused memory
        mov     bx, SEG zseg	; get number ending para of program + 1
	mov	ax, es		; get number  begining para of program
	sub	bx, ax		; calc size of program in para
	mov	ah, 4Ah		; realloc memory
	int	21h
        jnc     @F
	mov     si, offset mem_realloc_err_str
	call    print_str
	jmp	exit
@@:
endif

; get initial video mode
	mov	ah, 0Fh
	int	10h
    	mov	init_video_mode, al	; save current video mode

	push	es
; get base port address for active 6845 CRT controller
	mov	ax, 0040h
	mov	es, ax
	mov	ax, word ptr es:[63h]
	mov	indx_6845, ax

	add	ax, 4
	mov	mode_6845, ax	; addr of 6845 mode control reg

	inc	ax
	mov	color_6845, ax	; addr of 6845 color select reg

	inc	ax
	mov	status_6845, ax	; addr of 6845 status reg
	pop	es

;****************** get command line argument ********************************
	mov	al, byte ptr es:[80h]
	or	al, al		; if length of string is equal zero,
				; show usage and exit
	jnz	@F
	jmp	show_usage
@@:
	mov	cl, al		; mov length of string to cx
	mov	ch, 00h

	mov	si, 81h		; get addr of first letter to cx
	cld
cmdloop:
	lods	byte ptr es:[si]
	cmp	al, ' '		; if space or TAB (09h), loop
	je	@F
	cmp	al, 09h		; 09h - TAB code
@@:
	loope	cmdloop

	cmp	al, 0Dh		; if 0Dh exit
	jne	@F
	jmp	show_usage
@@:
	cmp	al, 0		; if 0 exit
	jne	@F
	jmp	show_usage
@@:
	or	al, 20h		; convert symbol to lower case

	cmp	al, '0'
	jne	@F
	and	al, 0Fh		; get mode number (0-F) in AL
	call	mode0123
        jmp     exit
@@:
	cmp	al, '1'
	jne	@F
	and	al, 0Fh		; get mode number (0-F) in AL
	call	mode0123
        jmp     exit
@@:
	cmp	al, '2'
	jne	@F
	and	al, 0Fh		; get mode number (0-F) in AL
	call	mode0123
        jmp     exit
@@:
	cmp	al, '3'
	jne	@F
	and	al, 0Fh		; get mode number (0-F) in AL
	call	mode0123
        jmp     exit
@@:
	cmp	al, '4'
	jne	@F
	and	al, 0Fh		; get mode number (0-F) in AL
	call	mode45
        jmp     exit
@@:
	cmp	al, '5'
	jne	@F
	and	al, 0Fh		; get mode number (0-F) in AL
	call	mode45
        jmp     exit
@@:
	cmp	al, '6'
	jne	@F
	and	al, 0Fh		; get mode number (0-F) in AL
	call	mode6
        jmp     exit
@@:
	cmp	al, '7'
	jne	@F
	and	al, 0Fh		; get mode number (0-F) in AL
	call	mode7
        jmp     exit
@@:
	cmp	al, 'c'
        jne	@F
        lea     dx, ntscdata_file_s
        call    ntsc_out         ; composite NTSC out test
        jmp     exit
@@:
	cmp	al, 'l'
	jne	@F
        lea     dx, c160100d_file_s
        call    c160_100         ; 160 x 100 color mode test
        jmp     exit
@@:
	cmp	al, 'h'
	jne	@F
        call    get_cmd_fname   ; get file name from cmd str to h_bmp_file_s
        lea     dx, h_bmp_file_s
        call    hgc_out				; Hercules test
        jmp     exit
@@:
        cmp     al, 't'
	jne	@F
        call    vhw_detect         ; video hardware detection
        jmp     exit
@@:
	cmp	al, 's'
	jne	show_usage
; take next symbol
	lods	byte ptr es:[si]
	or	al, 20h		; convert new symbol to lower case
	cmp	al, 'm'
	jne	@F
        call    mda_sw         ; switch to MDA/HGC
        jmp     exit
@@:         
        cmp     al, 'e'
	jne	@F
        lods    byte ptr es:[si] ; load new symbol
        and     al, 0Fh          ; convert to digit
        cmp     al, 3          ; check new symbol, if 0-1, set 40x25, 2-3 80x25
        ja      show_usage
        call    ega_sw          ; switch to EGA mode1-3
	jmp	exit

@@:
	cmp	al, 'c'
	jne	show_usage
	lods	byte ptr es:[si] ; load new symbol
        and     al, 0Fh          ; convert to digit
        cmp     al, 3          ; check new symbol, if 0-1, set 40x25, 2-3 80x25
        ja      show_usage
        call    cga_sw          ; switch to CGA mode1-3
	jmp	exit

; if valid modes were not found, then to print usage and exit
show_usage:
	mov	ah, 09h		; show message "usage"
	mov	dx, offset usage_str
	int	21h
	jmp	exit

;***************************Video cards detection***************************
vhw_detect proc near
;--------------------detect CGA 6845 compatible card------------------------
        mov     si, offset detect_cga_str
        call    print_str
        mov     dx, 3D4h        ; DX address of video controller port
	call	find6845
        mov     si, offset none_str
        jc     @F
        mov     si, offset ok_str
@@:
        call    print_str

;--------------------------detect EGA card------------------------------
        mov     si, offset detect_ega_str
        call    print_str
	mov	ax, 40h
	mov	es, ax
	mov	al, es:[87h]
        mov     si, offset none_str
	cmp	al, 0
        jz      ega_det_end
	test	al, 00001000b	; if EGA present, by inactive, bit3-> 1
	jnz	@F
	mov	act_card, EGA	; if bit3->0, EGA active
@@:
; detect EGA RAM size and print that EGA found
	mov	ah, 12h
        mov     bl, 10h
	int	10h
	and	bl, 03h
        mov     si, offset m64k_str
	cmp	bl, 00h	
	je	@F	
        mov     si, offset m128k_str
	cmp	bl, 01h	
	je	@F	
        mov     si, offset m192k_str
	cmp	bl, 02h	
	je	@F	
        mov     si, offset m256k_str
@@:
ega_det_end:
        call    print_str

;--------------------------detect MDA/HGC card------------------------------
        mov     si, offset detect_mono_str
        call    print_str
 	call	findmono	; (CF == 1) -> no HGC, no MDA
        mov     si, offset none_str
	jc	@F
        mov     si, offset mda_str
	cmp	al, 01h		; ((AL == 01h) && (CF==0)) -> MDA found
        je     @F
        mov     si, offset hgc_str
@@:
        call    print_str

;--------------------------detect active card------------------------------
        mov     si, offset active_card_str
        call    print_str
        mov     si, offset none_str
        cmp     act_card, EGA   ; if EGA active (earlier it was detected),
        jne     @F              ; print message and retn
        mov     si, offset ega_str
	call	print_str
	retn

